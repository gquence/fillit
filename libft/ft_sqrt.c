#include "libft.h"

# define ABS(x) (((x) < 0) ? (-(x)) : (x))

float 		ft_sqrt(int a)
{
    float y;

	y = a / 2;
    while (ABS(y * y - a) > 0.001)
        y = (a / y + y) / 2;
	return (y);
}